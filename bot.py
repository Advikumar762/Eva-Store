@Client.on_message(filters.command(['batch', 'pbatch']) & filters.create(allowed))
async def gen_link_batch(bot, message):
    if " " not in message.text:
        return await message.reply("Use correct format.\nExample <code>/batch https://t.me/kd_botz/10 https://t.me/kd_botz/20</code>.")
    links = message.text.strip().split(" ")
    if len(links) != 3:
        return await message.reply("Use correct format.\nExample <code>/batch https://t.me/kd_botz/10 https://t.me/kd_botz/20</code>.")
    cmd, first, last = links
    
    regex = re.compile("(https://)?(t\.me/|telegram\.me/|telegram\.dog/)(c/)?(\d+|[a-zA-Z_0-9]+)/(\d+)$")
    match = regex.match(first)
    
    if not match:
        return await message.reply('Invalid link')
        
    f_chat_id = match.group(4)
    f_msg_id = int(match.group(5))
    if f_chat_id.isnumeric():
        f_chat_id  = int(("-100" + f_chat_id))

    match = regex.match(last)
    if not match:
        return await message.reply('Invalid link')
        
    l_chat_id = match.group(4)
    l_msg_id = int(match.group(5))
    if l_chat_id.isnumeric():
        l_chat_id  = int(("-100" + l_chat_id))

    if f_chat_id != l_chat_id:
        return await message.reply("Chat ids not matched.")
        
    try:
        chat_id = (await bot.get_chat(f_chat_id)).id
        
    except ChannelInvalid:
        return await message.reply('This may be a private channel / group. Make me an admin over there to index the files.')
    except (UsernameInvalid, UsernameNotModified):
        return await message.reply('Invalid Link specified.')
    except Exception as e:
        return await message.reply(f'Errors - {e}')

    sts = await message.reply("Generating link for your message.\nThis may take time depending upon number of messages")
    
    if chat_id in FILE_STORE_CHANNEL:
        string = f"{f_msg_id}_{l_msg_id}_{chat_id}_{cmd.lower().strip()}"
        b_64 = base64.urlsafe_b64encode(string.encode("ascii")).decode().strip("=")
        return await sts.edit(f"Here is your link https://t.me/{temp.U_NAME}?start=DSTORE-{b_64}")

    FRMT = f"Generating Link...\nTotal Messages: `{total}`\nDone: `{current}`\nRemaining: `{rem}`\nStatus: `{sts}`"

    outlist = []

# file store without db channel
    og_msg = 0
    tot = 0
    for msg in bot.iter_messages(f_chat_id, l_msg_id, f_msg_id):
        tot += 1
        if msg.empty or msg.service:
            continue
        if not msg.media:
        # only media messages supported.
            continue
        try:
            file_type = msg.media
            file = getattr(msg, file_type.value)
            caption = getattr(msg, 'caption', '')
            if caption:
                caption = caption.html
            if file:
                file = {
                    "file_id": file.file_id,
                    "caption": caption,
                    "title": getattr(file, "file_name", ""),
                    "size": file.file_size,
                    "protect": cmd.lower().strip() == "/pbatch",
                }

                og_msg += 1
                outlist.append(file)

                # Forward the media file to DB_CHANNEL
                try:
                    sent_message = await bot.send_document(
                        chat_id=DB_CHANNEL,
                        document=file.file_id,
                        caption=caption,
                        disable_web_page_preview=True
                    )
                except Exception as e:
                # Handle any exceptions that might occur during forwarding
                    print(f"Error forwarding media: {e}")
        except:
            pass
    if not og_msg % 20:
        try:
            await sts.edit(FRMT.format(total=l_msg_id - f_msg_id, current=tot, rem=((l_msg_id - f_msg_id) - tot),
                                        sts="Saving Messages"))
        except:
            pass
    with open(f"batchmode_{message.from_user.id}.json", "w+") as out:
        json.dump(outlist, out)
    post = await bot.send_document(LOG_CHANNEL, f"batchmode_{message.from_user.id}.json", file_name="Batch.json", caption="⚠️Generated for filestore.")
    os.remove(f"batchmode_{message.from_user.id}.txt")
    file_id, ref = unpack_new_file_id(post.document.file_id)
    await sts.edit(f"Here is your link\nContains `{og_msg}` files.\n https://t.me/{temp.U_NAME}?start=BATCH-{file_id}")

@Client.on_message(filters.command(['json', 'j']) & filters.create(allowed))
async def json(bot, message):
    replied = message.reply_to_message
    if not replied:
        return await message.reply('Reply to a message to get a shareable link.')

    json_file_path = f"batchmode_{message.from_user.id}.json"
    txt_file_path = f"batchmode_{message.from_user.id}.txt"
        
    try:
        with open(msg.media, "r") as json_file:
            data = json.load(json_file)

        with open(txt_file_path, "w+") as txt_file:
            for item in data:
                txt_file.write(json.dumps(item) + '\n')

        # Remove the JSON file
        os.remove(json_file_path)
    except Exception as e:
        print(f"Error converting JSON to TXT: {e}")

